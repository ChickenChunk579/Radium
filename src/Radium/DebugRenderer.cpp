    Vector2f ConvertToRenderCoords(Vector2f inputPos, Radium::Nodes::CoordinateOrigin origin) {
        // Apply camera offset to world position
        Vector2f screenPos = inputPos;
        if (Radium::currentApplication) {
            screenPos = screenPos - Radium::currentApplication->GetCamera()->offset;
        }
        
        float scale = Radium::GetPixelScale();

        float x = screenPos.x * scale;
        float y = -screenPos.y * scale;

        if (origin == Radium::Nodes::CoordinateOrigin::Center) {
            x += Rune::windowWidth / 2.0f;
            y += Rune::windowHeight / 2.0f;
        }

        return Vector2f(x, y);
    }


